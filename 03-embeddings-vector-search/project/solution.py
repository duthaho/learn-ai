# Demo 5: Similarity Threshold
# ---------------------------------------------------------------------------

def demo_threshold(model: SentenceTransformer, chunks: list[str], index: faiss.IndexFlatIP):
    print("--- 5. Similarity Threshold Demo ---\n")

    irrelevant_query = "How do I make chocolate chip cookies?"
    print(f"  Query: \"{irrelevant_query}\"")
    print(f"  (This query has nothing to do with the documents.)\n")

    results = search(irrelevant_query, model, index, chunks, top_k=3)

    print("  Results WITHOUT threshold (vector search always returns something):")
    for rank, (chunk, score) in enumerate(results, 1):
        preview = chunk[:90].replace("\n", " ")
        print(f"    #{rank} (score: {score:.4f}): {preview}...")

    print()

    threshold = 0.35
    print(f"  Results WITH threshold (score >= {threshold}):")
    filtered = [(chunk, score) for chunk, score in results if score >= threshold]
    if filtered:
        for rank, (chunk, score) in enumerate(filtered, 1):
            preview = chunk[:90].replace("\n", " ")
            print(f"    #{rank} (score: {score:.4f}): {preview}...")
    else:
        print("    (no results above threshold — correctly rejected irrelevant query)")

    print()
    print("  Takeaway: vector search always returns the nearest neighbors, even")
    print("  if they are irrelevant. Use a similarity threshold to filter results.")
    print()


