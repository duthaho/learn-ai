# Demo 1: Embed and Inspect
# ---------------------------------------------------------------------------

def demo_embed_inspect(model: SentenceTransformer):
    print("--- 1. Embed and Inspect a Sentence ---\n")

    sentence = "Authentication is the process of verifying a user's identity."
    vec = embed([sentence], model)[0]

    print(f"  Sentence : {sentence}")
    print(f"  Dimension: {vec.shape[0]}")
    print(f"  First 10 : {np.round(vec[:10], 4).tolist()}")
    print(f"  L2 norm  : {np.linalg.norm(vec):.4f}  (should be ~1.0)")
    print()


# ---------------------------------------------------------------------------
# Demo 2: Semantic Similarity Matrix
# ---------------------------------------------------------------------------

def demo_similarity_matrix(model: SentenceTransformer):
    print("--- 2. Semantic Similarity Matrix ---\n")

    sentences = [
        "How do I reset my password?",
        "I forgot my login credentials.",
        "What is OAuth 2.0?",
        "How does third-party authentication work?",
        "What is the weather forecast for tomorrow?",
    ]

    vecs = embed(sentences, model)
    similarity = vecs @ vecs.T

    # Print labels
    labels = [f"S{i+1}" for i in range(len(sentences))]
    header = "       " + "  ".join(f"{l:>6}" for l in labels)
    print(header)

    for i, row in enumerate(similarity):
        vals = "  ".join(f"{v:6.3f}" for v in row)
        print(f"  {labels[i]}   {vals}")

    print()
    print("  Sentences:")
    for i, s in enumerate(sentences):
        print(f"    S{i+1}: {s}")

    print()
    print("  Note: S1-S2 and S3-S4 are semantically similar (high scores).")
    print("  S5 is unrelated to the others (low scores).")
    print()


