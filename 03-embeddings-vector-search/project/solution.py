DOCUMENT = """\
Getting Started with Authentication

Passwords and Hashing

Storing passwords in plain text is a critical security vulnerability. Always \
hash passwords using a modern algorithm such as bcrypt, scrypt, or Argon2. \
These algorithms are designed to be computationally expensive, which slows \
down brute-force attacks. Add a unique salt to each password before hashing \
to prevent rainbow table lookups. Never use fast hashing functions like MD5 \
or SHA-256 for password storage because they can be computed billions of \
times per second on modern hardware.

OAuth 2.0 and Third-Party Login

OAuth 2.0 allows users to log in using their existing accounts from providers \
like Google, GitHub, or Microsoft. The application redirects the user to the \
provider's login page, and after successful authentication, the provider \
returns an authorization code. The application exchanges this code for an \
access token, which grants limited access to the user's profile information. \
OAuth is preferred for consumer-facing applications because it eliminates the \
need for users to create yet another password. Always validate the state \
parameter to prevent cross-site request forgery attacks.

API Keys and Service Authentication

API keys are long, randomly generated strings used to authenticate \
machine-to-machine communication. Unlike passwords, API keys are not tied to \
a human user but to an application or service account. Store API keys in \
environment variables or a secrets manager, never in source code or version \
control. Rotate API keys regularly and implement key scoping so each key has \
only the minimum permissions required. Monitor API key usage for anomalies \
that might indicate a compromised key.

Session Management

After a user authenticates, the server creates a session to track the logged-in \
state. Sessions are typically stored server-side with a session ID sent to the \
client as an HTTP cookie. Set cookies with the Secure, HttpOnly, and SameSite \
flags to prevent interception and cross-site attacks. Implement session \
expiration with both idle timeouts and absolute timeouts. For stateless \
architectures, JSON Web Tokens (JWTs) can replace server-side sessions, but \
JWTs come with their own challenges: they cannot be revoked once issued unless \
you maintain a blocklist, and they increase the size of every request.

Multi-Factor Authentication

Multi-factor authentication (MFA) adds a second verification step beyond the \
password. Common second factors include time-based one-time passwords (TOTP) \
generated by apps like Google Authenticator, SMS codes, hardware security keys \
like YubiKey, and biometric verification. TOTP is preferred over SMS because \
SMS is vulnerable to SIM-swapping attacks. For high-security applications, \
require MFA for all users. For consumer applications, offer MFA as an option \
and encourage adoption through user education and simplified setup flows.\
"""


# ---------------------------------------------------------------------------
# Chunker
# ---------------------------------------------------------------------------

class RecursiveChunker:
    """Split text into chunks by paragraphs, then by sentences if needed."""

    def __init__(self, max_chunk_size: int = 300, overlap: int = 50):
        self.max_chunk_size = max_chunk_size
        self.overlap = overlap

    def chunk(self, text: str) -> list[str]:
        sections = [s.strip() for s in text.split("\n\n") if s.strip()]
        chunks = []
        for section in sections:
            if len(section) <= self.max_chunk_size:
                chunks.append(section)
            else:
                chunks.extend(self._split_by_sentences(section))
        return self._add_overlap(chunks)

    def _split_by_sentences(self, text: str) -> list[str]:
        sentences = []
        for part in text.replace("? ", "?|").replace(". ", ".|").replace("! ", "!|").split("|"):
            part = part.strip()
            if part:
                sentences.append(part)

        chunks, current = [], ""
        for sentence in sentences:
            candidate = f"{current} {sentence}".strip() if current else sentence
            if len(candidate) <= self.max_chunk_size:
                current = candidate
            else:
                if current:
                    chunks.append(current)
                current = sentence
        if current:
            chunks.append(current)
        return chunks

    def _add_overlap(self, chunks: list[str]) -> list[str]:
        if self.overlap == 0 or len(chunks) <= 1:
            return chunks
        result = [chunks[0]]
        for i in range(1, len(chunks)):
            prev_words = chunks[i - 1].split()
            overlap_words = prev_words[-self.overlap // 5:] if len(prev_words) > self.overlap // 5 else prev_words
            overlap_text = " ".join(overlap_words)
            result.append(f"{overlap_text} {chunks[i]}")
        return result


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def embed(texts: list[str], model: SentenceTransformer) -> np.ndarray:
    """Embed a list of texts and return L2-normalized vectors."""
    vectors = model.encode(texts, convert_to_numpy=True, normalize_embeddings=True)
    return vectors.astype("float32")


def build_index(vectors: np.ndarray) -> faiss.IndexFlatIP:
    """Build a FAISS inner-product index from normalized vectors."""
    dim = vectors.shape[1]
    index = faiss.IndexFlatIP(dim)
    index.add(vectors)
    return index


def search(query: str, model: SentenceTransformer, index: faiss.IndexFlatIP,
           chunks: list[str], top_k: int = 3) -> list[tuple[str, float]]:
    """Embed a query and return the top-k matching chunks with scores."""
    query_vec = embed([query], model)
    scores, indices = index.search(query_vec, top_k)
    return [(chunks[i], float(scores[0][j])) for j, i in enumerate(indices[0])]


