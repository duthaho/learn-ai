# Demo 3: Document Chunking
# ---------------------------------------------------------------------------

def demo_chunking():
    print("--- 3. Document Chunking ---\n")

    chunker = RecursiveChunker(max_chunk_size=300, overlap=50)
    chunks = chunker.chunk(DOCUMENT)

    for i, chunk in enumerate(chunks):
        print(f"  Chunk {i+1} ({len(chunk)} chars):")
        preview = chunk[:120].replace("\n", " ")
        if len(chunk) > 120:
            preview += "..."
        print(f"    {preview}")
    print()
    print(f"  Total chunks: {len(chunks)}")
    print()

    return chunks


# ---------------------------------------------------------------------------
# Demo 4: Build Index and Search
# ---------------------------------------------------------------------------

def demo_search(model: SentenceTransformer, chunks: list[str]):
    print("--- 4. Build Index and Search ---\n")

    vecs = embed(chunks, model)
    index = build_index(vecs)

    queries = [
        "How should I store passwords securely?",
        "What is the difference between OAuth and API keys?",
        "How do sessions and cookies work?",
    ]

    for query in queries:
        print(f"  Query: \"{query}\"")
        results = search(query, model, index, chunks, top_k=3)
        for rank, (chunk, score) in enumerate(results, 1):
            preview = chunk[:100].replace("\n", " ")
            print(f"    #{rank} (score: {score:.4f}): {preview}...")
        print()

    return index, vecs


